import numpy as np

def floors(array):
	np.set_printoptions(legacy='1.13')
	print(np.floor(array))
	return np.floor(array)
def ceils(array):
	np.set_printoptions(legacy='1.13')
	print(np.ceil(array))
	return np.ceil(array)
def rints(array):
	np.set_printoptions(legacy='1.13')
	print(np.rint(array))
	return np.rint(array)